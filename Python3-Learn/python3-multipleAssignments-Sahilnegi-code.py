# Multiple assignments


# 1- assigning same value to a multiple variable
a=b=c=10; 
print(a);
print(b);
print(c);
# 2- Assigning multiple values to multiple variable
x,y,z =10,20,30; ## assigning the value in order wise.
print(x);
print(y);
print(z);
